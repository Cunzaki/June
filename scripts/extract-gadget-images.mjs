#!/usr/bin/env node
/**
 * Extract image asset IDs from dump:
 *   - properties/ImageLabel.jsonl
 *   - decompiled Item/Gun/Utility module scripts (t.icon / t.image)
 *   - Globals.icons operator class icons
 *
 * Run: node scripts/extract-gadget-images.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const DUMP = path.join(ROOT, "dump");
const SCRIPTS = path.join(DUMP, "scripts");
const IMAGE_LABELS = path.join(DUMP, "properties/ImageLabel.jsonl");
const GLOBALS = path.join(SCRIPTS, "ReplicatedStorage.Modules.Globals.ModuleScript.lua");
const OUT_LUA = path.join(ROOT, "src/game/gadget_images.lua");
const MANIFEST = path.join(ROOT, "assets/manifest.json");

const RBX_RE = /rbxassetid:\/\/(\d+)/g;
const ICON_RE = /t\.(icon|image)\s*=\s*"(rbxassetid:\/\/\d+)"/g;

function rbxId(str) {
  if (!str) return null;
  const m = String(str).match(/(\d{5,})/);
  return m ? m[1] : null;
}

function add(byName, assetIds, name, id) {
  id = rbxId(id);
  if (!id) return;
  assetIds.add(id);
  if (name && !byName[name]) byName[name] = id;
}

function parseImageLabels(byName, assetIds) {
  if (!fs.existsSync(IMAGE_LABELS)) return;
  for (const line of fs.readFileSync(IMAGE_LABELS, "utf8").split("\n")) {
    if (!line.trim()) continue;
    let row;
    try { row = JSON.parse(line); } catch { continue; }
    const img = rbxId(row.properties?.Image);
    if (!img) continue;
    assetIds.add(img);
    const p = row.path || "";
    const leaf = row.name || p.split(".").pop();
    if (leaf) byName[leaf] = byName[leaf] || img;
    if (p.includes("TopList.Icon")) byName[`operator_${img}`] = img;
  }
}

function itemNameFromScript(file) {
  const base = path.basename(file, ".ModuleScript.lua");
  const parts = base.split(".");
  const idx = parts.indexOf("Item");
  if (idx >= 0) return parts[parts.length - 1];
  return base.split(".").pop();
}

function parseItemScripts(byName, assetIds) {
  if (!fs.existsSync(SCRIPTS)) return;
  let count = 0;
  for (const file of fs.readdirSync(SCRIPTS)) {
    if (!file.includes("Items.Item.") || !file.endsWith(".lua")) continue;
    const text = fs.readFileSync(path.join(SCRIPTS, file), "utf8");
    if (text.includes("Your Executor does NOT have a Decompiler")) continue;
    const name = itemNameFromScript(file);
    let m;
    ICON_RE.lastIndex = 0;
    while ((m = ICON_RE.exec(text)) !== null) {
      const field = m[1];
      add(byName, assetIds, `${name}_${field}`, m[2]);
      if (field === "icon") byName[name] = rbxId(m[2]);
      count++;
    }
  }
  return count;
}

function parseGlobalsIcons(byName, assetIds) {
  if (!fs.existsSync(GLOBALS)) return 0;
  const text = fs.readFileSync(GLOBALS, "utf8");
  const iconsBlock = text.match(/t\.icons\s*=\s*\{([\s\S]*?)\n\}/);
  if (!iconsBlock) return 0;
  let count = 0;
  const block = iconsBlock[1];
  const entryRe = /(\w+)\s*=\s*\{[^}]*image\s*=\s*"(rbxassetid:\/\/\d+)"/g;
  let m;
  while ((m = entryRe.exec(block)) !== null) {
    add(byName, assetIds, `class_${m[1]}`, m[2]);
    count++;
  }
  return count;
}

function parseAllScriptImages(byName, assetIds) {
  if (!fs.existsSync(GLOBALS)) return;
  const text = fs.readFileSync(GLOBALS, "utf8");
  let m;
  RBX_RE.lastIndex = 0;
  while ((m = RBX_RE.exec(text)) !== null) {
    assetIds.add(m[1]);
  }
}

function emitLua(byName, assetIds) {
  const ids = [...assetIds].sort((a, b) => (BigInt(a) < BigInt(b) ? -1 : 1));
  const lines = [
    "-- Auto-generated by scripts/extract-gadget-images.mjs",
    "-- Sources: ImageLabel.jsonl + Items modules + Globals.icons",
    "",
    "local M = {}",
    "",
    "M.ASSET_IDS = {",
  ];
  for (const id of ids) lines.push(`    ["${id}"] = true,`);
  lines.push("}", "", "M.BY_NAME = {");
  for (const [name, id] of Object.entries(byName).sort(([a], [b]) => a.localeCompare(b))) {
    lines.push(`    ["${name.replace(/"/g, '\\"')}"] = "${id}",`);
  }
  lines.push("}", "", "function M.get(name)", "    return M.BY_NAME[name]", "end", "", "return M");
  fs.mkdirSync(path.dirname(OUT_LUA), { recursive: true });
  fs.writeFileSync(OUT_LUA, lines.join("\n") + "\n");
  return ids;
}

function main() {
  const byName = {};
  const assetIds = new Set();

  parseImageLabels(byName, assetIds);
  const itemCount = parseItemScripts(byName, assetIds);
  const iconCount = parseGlobalsIcons(byName, assetIds);
  parseAllScriptImages(byName, assetIds);

  const ids = emitLua(byName, assetIds);
  fs.mkdirSync(path.dirname(MANIFEST), { recursive: true });
  fs.writeFileSync(
    MANIFEST,
    JSON.stringify({
      generated: new Date().toISOString(),
      source: "dump with decompiled scripts",
      assetIds: ids,
      count: ids.length,
      named: Object.keys(byName).length,
      from_items: itemCount,
      from_globals_icons: iconCount,
    }, null, 2) + "\n"
  );

  console.log(`Extracted ${ids.length} asset IDs, ${Object.keys(byName).length} named mappings`);
  console.log(`  Items modules: ${itemCount} icon/image fields`);
  console.log(`  Globals.icons: ${iconCount} operator classes`);
}

main();
